const PetNames = ["Chihuahua", "French bull dog", "Husky", "Robot Dog"];
const PetRarities = ["common", "rare", "legend", "mythical"];
const PetImages = ["common.png", "rare.png", "legend.png", "mythical.png"];

const fetchByTokenIds = async (req, res) => {
    try {
        const { tokenIds } = req.body;
        const nfts = await Pet.find({
            tokenId: {
                $in: tokenIds
            }
        });
        res.json({
            staus: true,
            data: nfts
        });
    } catch (err) {
        res.json({
            status: false,
            msg: err.message
        });
    }
}

const fetchByTokenId = async (req, res) => {
    try {
        const { id } = req.params;
        const nft = await Pet.findOne({
            tokenId: id
        });
        res.json({
            status: true,
            data: nft
        });
    } catch (err) {
        res.json({
            status: false,
            msg: err.message
        });
    }
}

const restoreNfts = async (req, res) => {
    try {
        console.log("===========ACCEPT PET REQUEST===========");
        let lastNft = await Pet.findOne().sort({ tokenId: -1 });
        const provider = new providers.JsonRpcProvider(RPC_URL)
        const wallet = new ethers.Wallet(PRIVATE_KEY, provider);
        const contract = new Contract(CONTRACT_ADDR, ABI, wallet);
        let totalSupply = await contract.totalSupply();
        totalSupply = Number(totalSupply);
        let tokenId = lastNft ? lastNft.tokenId : 0;
        console.log("TOKEN_ID======>", tokenId, "TOTAL_SUPPLY=======>", totalSupply);
        if (tokenId < totalSupply) {
            for (let id = tokenId + 1; id <= totalSupply; id++) {
                const tokenURI = await contract.tokenURI(id);
                const type = Number(tokenURI.slice(-6, -5))
                const nft = {
                    tokenId: id,
                    name: PetNames[type - 1],
                    rarity: PetRarities[type - 1],
                    image: PetImages[type - 1]
                }
                await Pet.create(nft);
            }
        }
        res.json({
            status: true,
            msg: "Successfully restored."
        });
    } catch (err) {
        res.json({
            status: false,
            msg: err.message
        });
    }
}


